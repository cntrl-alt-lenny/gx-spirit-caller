class TestRenderJson(unittest.TestCase):
    def test_json_schema(self):
        c = classify_report(_report([
            ("u", [_fn("u", "fn1", 95.0), _fn("u", "fn2", 50.0)]),
        ]))
        payload = render_json(c)
        # Round-trip.
        json.dumps(payload)
        self.assertEqual(payload["total_functions"], 2)
        self.assertIn("bucket_counts", payload)
        self.assertEqual(payload["bucket_counts"][BUCKET_ALMOST], 1)
        self.assertEqual(payload["bucket_counts"][BUCKET_MEDIUM], 1)
        self.assertIn("buckets", payload)
        self.assertEqual(len(payload["buckets"][BUCKET_ALMOST]), 1)
        self.assertEqual(
            payload["buckets"][BUCKET_ALMOST][0]["name"], "fn1",
        )
        self.assertIn("per_unit", payload)


