class TestClassifyReport(unittest.TestCase):
    def _fixture(self) -> dict:
        return _report([
            ("ov005.c", [
                _fn("ov005.c", "fn_matched", 100.0),
                _fn("ov005.c", "fn_almost", 98.0),
                _fn("ov005.c", "fn_almost2", 96.0),
            ]),
            ("ov006.c", [
                _fn("ov006.c", "fn_close", 80.0),
                _fn("ov006.c", "fn_medium", 50.0),
                _fn("ov006.c", "fn_low", 10.0),
                _fn("ov006.c", "fn_zero", 0.0),
            ]),
        ])

    def test_total_function_count(self):
        c = classify_report(self._fixture())
        self.assertEqual(c.total_functions, 7)

    def test_per_bucket_counts(self):
        c = classify_report(self._fixture())
        self.assertEqual(c.count(BUCKET_MATCHED), 1)
        self.assertEqual(c.count(BUCKET_ALMOST), 2)
        self.assertEqual(c.count(BUCKET_CLOSE), 1)
        self.assertEqual(c.count(BUCKET_MEDIUM), 1)
        self.assertEqual(c.count(BUCKET_LOW), 1)
        self.assertEqual(c.count(BUCKET_ZERO), 1)

    def test_per_module_distribution(self):
        c = classify_report(self._fixture())
        ov005 = c.per_module_buckets["ov005.c"]
        ov006 = c.per_module_buckets["ov006.c"]
        self.assertEqual(ov005[BUCKET_ALMOST], 2)
        self.assertEqual(ov005[BUCKET_MATCHED], 1)
        self.assertEqual(ov006[BUCKET_CLOSE], 1)
        self.assertEqual(ov006[BUCKET_ZERO], 1)

    def test_empty_report_yields_zero_counts(self):
        c = classify_report(_report([]))
        self.assertEqual(c.total_functions, 0)
        for b in BUCKET_ORDER:
            self.assertEqual(c.count(b), 0)


