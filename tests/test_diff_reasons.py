class TestRenderMarkdown(unittest.TestCase):
    def test_empty_report_still_renders(self):
        md = render_markdown(
            ClassifiedReport(total_functions=0, buckets={b: [] for b in BUCKET_ORDER}),
        )
        self.assertIn("Match-depth classification", md)
        self.assertIn(FOOTER_MARKER, md)

    def test_almost_bucket_headline_present(self):
        c = classify_report(_report([
            ("u", [_fn("u", "fn_at_97", 97.0)]),
        ]))
        md = render_markdown(c)
        self.assertIn("🟡 Almost-there (95-99%, 1)", md)
        self.assertIn("`fn_at_97`", md)
        self.assertIn("97.00%", md)

    def test_matched_bucket_hidden_by_default(self):
        # A report where every function is at 100% should render an
        # empty main body — the bucket summary hides matched by
        # default to keep the view action-oriented.
        c = classify_report(_report([
            ("u", [_fn("u", "fn1", 100.0), _fn("u", "fn2", 100.0)]),
        ]))
        md = render_markdown(c)
        self.assertNotIn("`matched`", md)
        # Count is still visible in the prose headline, though.
        self.assertIn("Classified **2** function(s)", md)

    def test_matched_bucket_shown_when_requested(self):
        c = classify_report(_report([
            ("u", [_fn("u", "fn1", 100.0)]),
        ]))
        md = render_markdown(c, include_matched=True)
        self.assertIn("`matched`", md)

    def test_top_n_truncation(self):
        # 30 almost-bucket fns, top=5 → only 5 rows + truncation note.
        fns = [_fn("u", f"fn_{i}", 95.0 + i * 0.1) for i in range(30)]
        c = classify_report(_report([("u", fns)]))
        md = render_markdown(c, top_n=5)
        # Newest fn has highest % and appears first.
        self.assertIn("`fn_29`", md)
        # Truncation note surfaces.
        self.assertIn("and 25 more", md)

    def test_per_unit_section_skips_zero_only_units(self):
        # A unit with only matched + zero functions shouldn't appear
        # in the per-unit heatmap (nothing actionable there).
        c = classify_report(_report([
            ("boring.c", [
                _fn("boring.c", "a", 100.0),
                _fn("boring.c", "b", 0.0),
            ]),
            ("interesting.c", [
                _fn("interesting.c", "c", 90.0),
            ]),
        ]))
        md = render_markdown(c)
        self.assertIn("`interesting.c`", md)
        # boring.c has no non-0 non-100 entries.
        self.assertNotIn("| `boring.c` |", md)


