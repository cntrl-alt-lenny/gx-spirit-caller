"""Unit tests for tools/diff_reasons.py.

The tool's job is to turn an opaque `47.3%` into actionable buckets
for decomp-triage. A regression in the bucket boundaries (or the
`iter_function_entries` parser being too strict on missing fields)
would give the decomper wrong signals about what's close-to-matching
— nasty and hard to spot downstream. These tests pin:

  - classify() boundary behaviour at every edge (0, 0.01, 25, 75, 95,
    99.999, 100)
  - load_report() graceful failure on missing / malformed files
  - iter_function_entries() defensive parsing (missing fields, zero
    fuzzy, null fuzzy, empty units list)
  - sort order (bucket rank → match% desc → unit → name)
  - classify_report per-module breakdown
  - render_markdown structure (headline, almost bucket visible,
    matched bucket hidden by default, footer marker)
  - render_json schema (bucket_counts total_functions etc.)
"""

from __future__ import annotations

import json
import sys
import tempfile
import unittest
from pathlib import Path

_TOOLS = Path(__file__).resolve().parent.parent / "tools"
sys.path.insert(0, str(_TOOLS))

from diff_reasons import (  # noqa: E402
    BUCKET_ALMOST,
    BUCKET_CLOSE,
    BUCKET_LOW,
    BUCKET_MATCHED,
    BUCKET_MEDIUM,
    BUCKET_ORDER,
    BUCKET_ZERO,
    ClassifiedReport,
    FunctionEntry,
    classify,
    classify_report,
    iter_function_entries,
    load_report,
    render_json,
    render_markdown,
)


FOOTER_MARKER = "<sub>🤖 Generated by `tools/diff_reasons.py`"


def _fn(
    unit: str, name: str, pct: float, size: int = 0x10,
) -> dict:
    """Build one function entry in the shape objdiff emits."""
    return {
        "name": name,
        "size": size,
        "fuzzy_match_percent": pct,
    }


def _report(units: list[tuple[str, list[dict]]]) -> dict:
    """Build a minimal report.json dict from (unit_name, functions)
    pairs. Follows objdiff's schema from protos/report.proto."""
    return {
        "version": 2,
        "measures": {},
        "units": [
            {"name": name, "functions": fns} for name, fns in units
        ],
    }


# ------------------------------------------------------------------------- #
# classify() boundaries
# ------------------------------------------------------------------------- #


class TestClassify(unittest.TestCase):
    def test_zero_is_zero_bucket(self):
        self.assertEqual(classify(0.0), BUCKET_ZERO)

    def test_just_above_zero_is_low(self):
        self.assertEqual(classify(0.01), BUCKET_LOW)

    def test_low_upper_boundary(self):
        # 25 is the threshold into medium, so 24.999 is the last low.
        self.assertEqual(classify(24.999), BUCKET_LOW)
        self.assertEqual(classify(25.0), BUCKET_MEDIUM)

    def test_medium_upper_boundary(self):
        self.assertEqual(classify(74.999), BUCKET_MEDIUM)
        self.assertEqual(classify(75.0), BUCKET_CLOSE)

    def test_close_upper_boundary(self):
        self.assertEqual(classify(94.999), BUCKET_CLOSE)
        self.assertEqual(classify(95.0), BUCKET_ALMOST)

    def test_almost_upper_boundary_below_matched(self):
        self.assertEqual(classify(99.0), BUCKET_ALMOST)
        self.assertEqual(classify(99.998), BUCKET_ALMOST)

    def test_matched_epsilon_absorbs_float_noise(self):
        # protobuf wire float may deliver 99.9999 from a 100.0 encode.
        # The matched bucket must absorb that.
        self.assertEqual(classify(99.9999), BUCKET_MATCHED)
        self.assertEqual(classify(100.0), BUCKET_MATCHED)


