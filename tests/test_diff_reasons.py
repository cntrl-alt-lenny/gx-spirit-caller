class TestIterFunctionEntries(unittest.TestCase):
    def test_empty_report(self):
        self.assertEqual(iter_function_entries(_report([])), [])

    def test_missing_fuzzy_treated_as_zero(self):
        # objdiff's protobuf may omit zero-valued fuzzy_match_percent
        # when serialising JSON. Tolerate that.
        r = {
            "units": [{
                "name": "u",
                "functions": [{"name": "fn", "size": 4}],
            }],
        }
        out = iter_function_entries(r)
        self.assertEqual(len(out), 1)
        self.assertEqual(out[0].fuzzy_match_percent, 0.0)
        self.assertEqual(out[0].bucket, BUCKET_ZERO)

    def test_null_fuzzy_treated_as_zero(self):
        r = _report([("u", [{"name": "fn", "size": 4,
                             "fuzzy_match_percent": None}])])
        out = iter_function_entries(r)
        self.assertEqual(out[0].fuzzy_match_percent, 0.0)

    def test_sort_bucket_rank_first(self):
        r = _report([
            ("u", [
                _fn("u", "zz_at_100", 100.0),   # matched last
                _fn("u", "aa_at_95", 95.0),      # almost first
                _fn("u", "bb_at_80", 80.0),      # close middle
            ]),
        ])
        out = iter_function_entries(r)
        # Expected order: almost, close, matched.
        buckets = [e.bucket for e in out]
        self.assertEqual(
            buckets,
            [BUCKET_ALMOST, BUCKET_CLOSE, BUCKET_MATCHED],
        )

    def test_sort_within_bucket_by_fuzzy_desc(self):
        r = _report([
            ("u", [
                _fn("u", "lower_almost", 95.1),
                _fn("u", "higher_almost", 99.5),
            ]),
        ])
        out = iter_function_entries(r)
        # Higher match% first within the bucket.
        self.assertEqual(out[0].name, "higher_almost")
        self.assertEqual(out[1].name, "lower_almost")

    def test_missing_unit_name_has_fallback(self):
        # Defensive: if a unit has no name field, still produce an
        # entry with a clearly-flagged placeholder.
        r = {
            "units": [
                {"functions": [_fn("", "orphan", 50.0)]},
            ],
        }
        out = iter_function_entries(r)
        self.assertEqual(len(out), 1)
        self.assertEqual(out[0].unit, "<unknown>")


