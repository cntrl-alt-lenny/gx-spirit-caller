class TestRenderJson(unittest.TestCase):
    def test_schema(self):
        before = {("u", "fn_reg"): _entry("u", "fn_reg", 100.0)}
        after = {
            ("u", "fn_reg"): _entry("u", "fn_reg", 80.0),
            ("u", "fn_new"): _entry("u", "fn_new", 50.0),
        }
        delta = compute_delta(before, after)
        payload = render_json(delta)
        # Round-trip.
        json.dumps(payload)
        self.assertIn("bucket_counts_before", payload)
        self.assertIn("bucket_counts_after", payload)
        self.assertIn("transitions", payload)
        self.assertIn("regressions", payload)
        self.assertIn("improvements", payload)
        self.assertIn("new", payload)
        self.assertIn("dropped", payload)
        self.assertEqual(len(payload["regressions"]), 1)
        self.assertEqual(len(payload["new"]), 1)
        self.assertEqual(payload["regressions"][0]["before"]["bucket"],
                         BUCKET_MATCHED)
        self.assertEqual(payload["regressions"][0]["after"]["bucket"],
                         BUCKET_CLOSE)


