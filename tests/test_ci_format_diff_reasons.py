"""Unit tests for tools/ci_format_diff_reasons.py.

Regressions in this renderer would spam PR comments (once CI is
wired) with wrong alarm signals — the "N regressions" headline is
load-bearing for decomper and brain attention. Pins:

  - extract_snapshot: (unit, name) keying; defensive parsing
  - compute_delta: regression / improvement / new / dropped / stable
    classification; bucket counts and transition matrix correctness
  - Movement properties: is_regression, is_improvement, pct_delta,
    is_new, is_dropped all behave at the boundary cases
  - render_markdown: empty diff / regression headline / improvement
    headline / dropped-entries section / footer marker
  - render_json: schema integrity, round-trip
  - main() is robust to non-UTF-8 stdout (same guard as the
    worklist-diff tool)
"""

from __future__ import annotations

import contextlib
import io
import json
import sys
import tempfile
import unittest
from pathlib import Path
from unittest.mock import patch

_TOOLS = Path(__file__).resolve().parent.parent / "tools"
sys.path.insert(0, str(_TOOLS))

from ci_format_diff_reasons import (  # noqa: E402
    Movement,
    SnapshotEntry,
    compute_delta,
    extract_snapshot,
    render_json,
    render_markdown,
)
from diff_reasons import (  # noqa: E402
    BUCKET_CLOSE,
    BUCKET_LOW,
    BUCKET_MATCHED,
    BUCKET_MEDIUM,
    BUCKET_ZERO,
)


FOOTER_MARKER = (
    "<sub>🤖 Generated by `tools/ci_format_diff_reasons.py`"
)


def _fn(name: str, pct: float, size: int = 0x10) -> dict:
    return {
        "name": name,
        "size": size,
        "fuzzy_match_percent": pct,
    }


def _report(units: list[tuple[str, list[dict]]]) -> dict:
    return {
        "version": 2,
        "units": [{"name": u, "functions": fns} for u, fns in units],
    }


def _entry(unit: str, name: str, pct: float, size: int = 0x10) -> SnapshotEntry:
    return SnapshotEntry(
        unit=unit, name=name, size=size, fuzzy_match_percent=pct,
    )


