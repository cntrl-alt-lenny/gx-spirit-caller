class TestExtractSnapshot(unittest.TestCase):
    def test_empty(self):
        self.assertEqual(extract_snapshot({}), {})

    def test_basic_shape(self):
        r = _report([("ov005.c", [_fn("fn", 75.0)])])
        snap = extract_snapshot(r)
        self.assertEqual(len(snap), 1)
        e = snap[("ov005.c", "fn")]
        self.assertEqual(e.fuzzy_match_percent, 75.0)
        self.assertEqual(e.bucket, BUCKET_CLOSE)

    def test_missing_fuzzy_defaults_to_zero(self):
        r = {"units": [{"name": "u", "functions": [{"name": "fn", "size": 4}]}]}
        snap = extract_snapshot(r)
        self.assertEqual(snap[("u", "fn")].fuzzy_match_percent, 0.0)
        self.assertEqual(snap[("u", "fn")].bucket, BUCKET_ZERO)

    def test_null_fuzzy_treated_as_zero(self):
        r = _report([("u", [{"name": "fn", "size": 4,
                             "fuzzy_match_percent": None}])])
        self.assertEqual(
            extract_snapshot(r)[("u", "fn")].fuzzy_match_percent, 0.0,
        )

    def test_duplicate_name_last_wins(self):
        # Two functions with the same (unit, name) — last one wins.
        # Doesn't happen in practice but documenting the behaviour.
        r = _report([
            ("u", [_fn("fn", 50.0), _fn("fn", 90.0)]),
        ])
        snap = extract_snapshot(r)
        self.assertEqual(
            snap[("u", "fn")].fuzzy_match_percent, 90.0,
        )


