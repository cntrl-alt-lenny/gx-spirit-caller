class TestComputeDelta(unittest.TestCase):
    def test_identity_no_movements(self):
        snap = {
            ("u", "fn"): _entry("u", "fn", 100.0),
        }
        delta = compute_delta(snap, dict(snap))
        self.assertEqual(delta.movements, [])
        self.assertEqual(delta.regressions, [])
        self.assertEqual(delta.improvements, [])
        self.assertEqual(delta.new_entries, [])
        self.assertEqual(delta.dropped_entries, [])

    def test_regression_flagged(self):
        # matched → close = regression.
        before = {("u", "fn"): _entry("u", "fn", 100.0)}
        after = {("u", "fn"): _entry("u", "fn", 80.0)}
        delta = compute_delta(before, after)
        self.assertEqual(len(delta.regressions), 1)
        reg = delta.regressions[0]
        self.assertEqual(reg.bucket_before, BUCKET_MATCHED)
        self.assertEqual(reg.bucket_after, BUCKET_CLOSE)
        self.assertAlmostEqual(reg.pct_delta, -20.0)

    def test_improvement_flagged(self):
        before = {("u", "fn"): _entry("u", "fn", 50.0)}
        after = {("u", "fn"): _entry("u", "fn", 100.0)}
        delta = compute_delta(before, after)
        self.assertEqual(len(delta.improvements), 1)
        imp = delta.improvements[0]
        self.assertEqual(imp.bucket_before, BUCKET_MEDIUM)
        self.assertEqual(imp.bucket_after, BUCKET_MATCHED)

    def test_same_bucket_pct_change_not_regression(self):
        # 98% → 96% stays in the ALMOST bucket. It's technically a
        # small backslide but doesn't change the bucket, so it's
        # neither flagged as regression nor improvement. The raw
        # movement IS recorded though, for the JSON delta.
        before = {("u", "fn"): _entry("u", "fn", 98.0)}
        after = {("u", "fn"): _entry("u", "fn", 96.0)}
        delta = compute_delta(before, after)
        self.assertEqual(delta.regressions, [])
        self.assertEqual(delta.improvements, [])
        # But the pct change was captured:
        self.assertEqual(len(delta.movements), 1)
        self.assertAlmostEqual(delta.movements[0].pct_delta, -2.0)

    def test_new_entry(self):
        before = {}
        after = {("u", "fn"): _entry("u", "fn", 50.0)}
        delta = compute_delta(before, after)
        self.assertEqual(len(delta.new_entries), 1)
        self.assertEqual(delta.new_entries[0].name, "fn")
        self.assertEqual(delta.regressions, [])

    def test_dropped_entry(self):
        before = {("u", "fn"): _entry("u", "fn", 100.0)}
        after = {}
        delta = compute_delta(before, after)
        self.assertEqual(len(delta.dropped_entries), 1)
        self.assertEqual(delta.dropped_entries[0].name, "fn")
        # A dropped entry is NOT counted as a regression (it's
        # renamed/removed, distinct class of event).
        self.assertEqual(delta.regressions, [])

    def test_bucket_counts_correct(self):
        before = {
            ("u", "a"): _entry("u", "a", 100.0),
            ("u", "b"): _entry("u", "b", 50.0),
            ("u", "c"): _entry("u", "c", 10.0),
        }
        after = {
            ("u", "a"): _entry("u", "a", 100.0),
            ("u", "b"): _entry("u", "b", 100.0),   # improvement
            ("u", "c"): _entry("u", "c", 10.0),
        }
        delta = compute_delta(before, after)
        self.assertEqual(delta.bucket_counts_before[BUCKET_MATCHED], 1)
        self.assertEqual(delta.bucket_counts_before[BUCKET_MEDIUM], 1)
        self.assertEqual(delta.bucket_counts_before[BUCKET_LOW], 1)
        self.assertEqual(delta.bucket_counts_after[BUCKET_MATCHED], 2)
        self.assertEqual(delta.bucket_counts_after[BUCKET_MEDIUM], 0)
        self.assertEqual(delta.bucket_counts_after[BUCKET_LOW], 1)

    def test_transitions_matrix(self):
        before = {
            ("u", "a"): _entry("u", "a", 100.0),  # matched
            ("u", "b"): _entry("u", "b", 100.0),  # matched
            ("u", "c"): _entry("u", "c", 50.0),   # medium
        }
        after = {
            ("u", "a"): _entry("u", "a", 100.0),   # matched → matched
            ("u", "b"): _entry("u", "b", 80.0),    # matched → close
            ("u", "c"): _entry("u", "c", 100.0),    # medium → matched
        }
        delta = compute_delta(before, after)
        # matched → matched: 1
        self.assertEqual(delta.transitions[BUCKET_MATCHED][BUCKET_MATCHED], 1)
        # matched → close: 1
        self.assertEqual(delta.transitions[BUCKET_MATCHED][BUCKET_CLOSE], 1)
        # medium → matched: 1
        self.assertEqual(delta.transitions[BUCKET_MEDIUM][BUCKET_MATCHED], 1)


