class TestRenderMarkdown(unittest.TestCase):
    def test_empty_delta_no_movements(self):
        # Identity diff: same snapshots both sides, nothing moves.
        delta = compute_delta({}, {})
        md = render_markdown(delta)
        self.assertIn("No bucket movements", md)
        self.assertIn(FOOTER_MARKER, md)

    def test_regression_headline_renders(self):
        before = {("u", "fn"): _entry("u", "fn", 100.0)}
        after = {("u", "fn"): _entry("u", "fn", 30.0)}
        delta = compute_delta(before, after)
        md = render_markdown(delta)
        self.assertIn("🛑 **1 regression(s)**", md)
        self.assertIn("### 🛑 Regressions (1)", md)
        self.assertIn("`fn`", md)
        self.assertIn("matched", md)
        self.assertIn("medium", md)
        self.assertIn("-70.00pp", md)

    def test_improvement_section(self):
        before = {("u", "fn"): _entry("u", "fn", 50.0)}
        after = {("u", "fn"): _entry("u", "fn", 100.0)}
        delta = compute_delta(before, after)
        md = render_markdown(delta)
        self.assertIn("✅ **1 improvement(s)**", md)
        self.assertIn("### ✅ Improvements (1)", md)
        self.assertIn("+50.00pp", md)

    def test_dropped_section_warns_about_footgun(self):
        before = {("u", "fn"): _entry("u", "fn", 100.0)}
        after = {}
        delta = compute_delta(before, after)
        md = render_markdown(delta)
        self.assertIn("🗑️ Dropped entries", md)
        # The surface warning about symbol renames is load-bearing —
        # it points at the exact class of bug that happens easily.
        self.assertIn("rename-breaks-everything", md)

    def test_new_section_surfaces(self):
        before = {}
        after = {("u", "fn"): _entry("u", "fn", 50.0)}
        delta = compute_delta(before, after)
        md = render_markdown(delta)
        self.assertIn("🆕 New entries", md)
        self.assertIn("`fn`", md)

    def test_top_n_truncation(self):
        # 30 regressions, top_n=5 → only 5 shown with truncation note.
        before = {
            ("u", f"fn_{i}"): _entry("u", f"fn_{i}", 100.0)
            for i in range(30)
        }
        after = {
            ("u", f"fn_{i}"): _entry("u", f"fn_{i}", 10.0)
            for i in range(30)
        }
        delta = compute_delta(before, after)
        md = render_markdown(delta, top_n=5)
        self.assertIn("### 🛑 Regressions (30)", md)
        self.assertIn("and 25 more regressions", md)

    def test_footer_always_present(self):
        for (before, after) in [
            ({}, {}),
            ({("u", "f"): _entry("u", "f", 100.0)}, {}),
            ({}, {("u", "f"): _entry("u", "f", 100.0)}),
        ]:
            md = render_markdown(compute_delta(before, after))
            self.assertIn(FOOTER_MARKER, md)


