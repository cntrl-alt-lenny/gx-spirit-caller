class TestMovement(unittest.TestCase):
    def test_is_regression_by_depth(self):
        m = Movement(
            entry_before=_entry("u", "fn", 100.0),
            entry_after=_entry("u", "fn", 80.0),
        )
        self.assertTrue(m.is_regression)
        self.assertFalse(m.is_improvement)

    def test_is_improvement_by_depth(self):
        m = Movement(
            entry_before=_entry("u", "fn", 80.0),
            entry_after=_entry("u", "fn", 100.0),
        )
        self.assertTrue(m.is_improvement)
        self.assertFalse(m.is_regression)

    def test_new_is_neither(self):
        m = Movement(entry_before=None, entry_after=_entry("u", "fn", 50.0))
        self.assertTrue(m.is_new)
        self.assertFalse(m.is_regression)
        self.assertFalse(m.is_improvement)

    def test_dropped_is_neither(self):
        m = Movement(entry_before=_entry("u", "fn", 50.0), entry_after=None)
        self.assertTrue(m.is_dropped)
        self.assertFalse(m.is_regression)

    def test_pct_delta_none_when_either_missing(self):
        m = Movement(entry_before=None, entry_after=_entry("u", "fn", 50.0))
        self.assertIsNone(m.pct_delta)

    def test_pct_delta_correct(self):
        m = Movement(
            entry_before=_entry("u", "fn", 60.0),
            entry_after=_entry("u", "fn", 85.0),
        )
        self.assertAlmostEqual(m.pct_delta, 25.0)


