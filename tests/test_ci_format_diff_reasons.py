class TestMainUtf8Stdout(unittest.TestCase):
    """Same guard as the worklist-diff tool (PR #95). Emoji headers
    (📉 🛑 ✅ 🆕 🗑️) would crash on a Windows cp1252 console without
    the reconfigure."""

    def test_main_with_cp1252_stdout_does_not_crash(self):
        from ci_format_diff_reasons import main
        with tempfile.TemporaryDirectory() as td:
            before = Path(td) / "b.json"
            after = Path(td) / "a.json"
            before.write_text(json.dumps(
                _report([("u", [_fn("fn", 100.0)])])
            ))
            after.write_text(json.dumps(
                _report([("u", [_fn("fn", 50.0)])])
            ))
            buf = io.BytesIO()
            ascii_stdout = io.TextIOWrapper(
                buf, encoding="cp1252", errors="strict",
                write_through=True,
            )
            argv = [
                "ci_format_diff_reasons.py",
                str(before), str(after),
            ]
            with patch.object(sys, "argv", argv), \
                 contextlib.redirect_stdout(ascii_stdout):
                rc = main()
            ascii_stdout.flush()
            self.assertEqual(rc, 0)
            # Output contains utf-8-encoded emoji bytes.
            self.assertIn(b"\xf0\x9f\x93\x89", buf.getvalue())  # 📉

    def test_main_missing_before_renders_warning(self):
        from ci_format_diff_reasons import main
        with tempfile.TemporaryDirectory() as td:
            after = Path(td) / "a.json"
            after.write_text(json.dumps(
                _report([("u", [_fn("fn", 100.0)])])
            ))
            buf = io.StringIO()
            argv = [
                "ci_format_diff_reasons.py",
                str(Path(td) / "missing.json"), str(after),
            ]
            with patch.object(sys, "argv", argv), \
                 contextlib.redirect_stdout(buf):
                rc = main()
            self.assertEqual(rc, 0)
            out = buf.getvalue()
            self.assertIn("Snapshot missing", out)
            # The after snapshot's fn shows up as "new" when before
            # is empty — sanity check the fallback rendering.
            self.assertIn("`fn`", out)


