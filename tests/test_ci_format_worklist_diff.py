"""Unit tests for tools/ci_format_worklist_diff.py.

The CI comment pipeline pipes render() output straight into `gh pr
comment`. Regressions here would spam every decomp PR with malformed
markdown. Pinning:

  - JSON loader tolerates malformed rows + parses hex addrs
  - compute_diff: landed / new / migrations classified correctly
    by (module, addr) identity
  - Sorting is deterministic (tier rank → size → addr)
  - render() covers: no-changes case, mixed case, per-section
    limit truncation, tier-summary bold-on-nonzero, footer marker
  - main() is robust to non-UTF-8 stdout (Windows cp1252 consoles)
"""

from __future__ import annotations

import io
import json
import sys
import tempfile
import unittest
from contextlib import redirect_stdout
from pathlib import Path
from unittest.mock import patch

_TOOLS = Path(__file__).resolve().parent.parent / "tools"
sys.path.insert(0, str(_TOOLS))

from ci_format_worklist_diff import (  # noqa: E402
    TIER_ORDER,
    WorklistDiff,
    WorklistEntry,
    _entry_sort_key,
    _load_breakdown,
    _load_entries,
    _parse_addr,
    _signed,
    _tier_rank,
    compute_diff,
    render,
)


FOOTER_MARKER = (
    "<sub>🤖 Generated by `tools/ci_format_worklist_diff.py`"
)


def _entry(module: str, addr: int, **kwargs) -> WorklistEntry:
    return WorklistEntry(
        tier=kwargs.get("tier", "easy"),
        module=module,
        addr=addr,
        size=kwargs.get("size", 0x10),
        name=kwargs.get("name", f"func_{module}_{addr:08x}"),
    )


def _write_snapshot(path: Path, worklist: list[dict],
                    breakdown: dict | None = None) -> None:
    payload = {
        "version": "eur",
        "tier_breakdown": breakdown or {},
        "worklist": worklist,
    }
    path.write_text(json.dumps(payload))


