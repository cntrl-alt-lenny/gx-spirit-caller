class TestRender(unittest.TestCase):
    def test_no_changes(self):
        d = WorklistDiff(
            landed=[], new=[], migrations=[],
            tier_summary={t: (0, 0) for t in TIER_ORDER},
        )
        md = render(d)
        self.assertIn("📉 Worklist delta", md)
        self.assertIn("No worklist changes", md)
        self.assertIn(FOOTER_MARKER, md)
        # No "matches landed" / "new candidates" sections.
        self.assertNotIn("Matches landed", md)
        self.assertNotIn("New worklist entries", md)
        self.assertNotIn("Tier migrations", md)

    def test_only_tier_summary_when_counts_shift_without_worklist_changes(self):
        # Edge case: breakdown totals changed but no individual worklist
        # identity shifted. Can happen when someone adds a TU carve
        # without matching anything. Tier summary should render; the
        # three other sections should not.
        d = WorklistDiff(
            landed=[], new=[], migrations=[],
            tier_summary={"easy": (55, 55), "trivial": (75, 78)},
        )
        md = render(d)
        self.assertIn("Tier matched-count delta", md)
        self.assertIn("**+3**", md)  # trivial delta bolded

    def test_full_mixed_output(self):
        landed = [
            _entry("ov005", 0x100, tier="easy", size=0x10, name="landed_a"),
            _entry("ov006", 0x200, tier="easy", size=0x10, name="landed_b"),
        ]
        new = [_entry("ov015", 0x1234, tier="easy", name="new_one")]
        mig = (
            _entry("ov011", 0xABC, tier="medium", name="migrated"),
            _entry("ov011", 0xABC, tier="named", name="migrated"),
        )
        d = WorklistDiff(
            landed=landed, new=new, migrations=[mig],
            tier_summary={
                "trivial": (0, 0),
                "easy":    (55, 59),
                "sinit":   (46, 46),
            },
        )
        md = render(d)
        # Headline count.
        self.assertIn("2 match(es) landed", md)
        self.assertIn("1 new candidate(s)", md)
        self.assertIn("1 tier migration(s)", md)
        # Section presence.
        self.assertIn("✅ Matches landed (2)", md)
        self.assertIn("🆕 New worklist entries (1)", md)
        self.assertIn("🔀 Tier migrations (1)", md)
        # Specific rows rendered.
        self.assertIn("`landed_a`", md)
        self.assertIn("`landed_b`", md)
        self.assertIn("`new_one`", md)
        self.assertIn("`medium`", md)
        self.assertIn("`named`", md)
        # Tier summary: easy Δ is +4 (bold), sinit Δ is 0 (plain).
        self.assertIn("**+4**", md)
        self.assertNotIn("**0**", md)
        # Footer.
        self.assertIn(FOOTER_MARKER, md)

    def test_per_section_limit_with_truncation(self):
        many = [
            _entry("ov006", 0x100 + i * 4, name=f"fn_{i}", tier="trivial")
            for i in range(50)
        ]
        d = WorklistDiff(
            landed=many, new=[], migrations=[],
            tier_summary={"trivial": (0, 50)},
        )
        md = render(d, limit_per_section=10)
        # First 10 rendered.
        self.assertIn("`fn_0`", md)
        self.assertIn("`fn_9`", md)
        # 11th onward should NOT appear.
        self.assertNotIn("`fn_10`", md)
        # Truncation note.
        self.assertIn("_…and 40 more matches landed_", md)


class TestFooterMarker(unittest.TestCase):
    def test_marker_in_every_render(self):
        """The footer marker is load-bearing — any CI wiring will use
        it to upsert a single comment instead of spamming on push."""
        for d in [
            WorklistDiff(landed=[], new=[], migrations=[],
                         tier_summary={}),
            WorklistDiff(
                landed=[_entry("ov005", 0x100)],
                new=[], migrations=[],
                tier_summary={"easy": (0, 1)},
            ),
        ]:
            md = render(d)
            self.assertIn(FOOTER_MARKER, md)


