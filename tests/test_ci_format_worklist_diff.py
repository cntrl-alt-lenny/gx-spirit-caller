class TestComputeDiff(unittest.TestCase):
    def test_identity_no_changes(self):
        before = {("ov006", 0x100): _entry("ov006", 0x100)}
        after = dict(before)
        d = compute_diff(before, after, {}, {})
        self.assertEqual(d.landed, [])
        self.assertEqual(d.new, [])
        self.assertEqual(d.migrations, [])

    def test_match_landed_detection(self):
        # Item present in before, absent in after → landed.
        before = {
            ("ov005", 0x100): _entry("ov005", 0x100),
            ("ov005", 0x200): _entry("ov005", 0x200),
        }
        after = {("ov005", 0x200): _entry("ov005", 0x200)}
        d = compute_diff(before, after, {}, {})
        self.assertEqual(len(d.landed), 1)
        self.assertEqual(d.landed[0].addr, 0x100)
        self.assertEqual(d.new, [])

    def test_new_entry_detection(self):
        before = {}
        after = {("ov006", 0x300): _entry("ov006", 0x300)}
        d = compute_diff(before, after, {}, {})
        self.assertEqual(d.landed, [])
        self.assertEqual(len(d.new), 1)
        self.assertEqual(d.new[0].addr, 0x300)

    def test_tier_migration_detection(self):
        before = {("ov011", 0x100): _entry("ov011", 0x100, tier="medium")}
        after = {("ov011", 0x100): _entry("ov011", 0x100, tier="named")}
        d = compute_diff(before, after, {}, {})
        self.assertEqual(d.landed, [])
        self.assertEqual(d.new, [])
        self.assertEqual(len(d.migrations), 1)
        b, a = d.migrations[0]
        self.assertEqual(b.tier, "medium")
        self.assertEqual(a.tier, "named")

    def test_landed_sorted_easiest_first(self):
        # Two landed items: trivial (small) + easy (large). trivial
        # must come first.
        before = {
            ("ov005", 0x100): _entry("ov005", 0x100, tier="easy", size=0x40),
            ("ov006", 0x200): _entry("ov006", 0x200, tier="trivial", size=0x4),
        }
        after = {}
        d = compute_diff(before, after, {}, {})
        self.assertEqual([e.tier for e in d.landed], ["trivial", "easy"])

    def test_tier_summary_union_of_tiers(self):
        # TIER_ORDER tiers always appear; unknowns present in either
        # breakdown also surface.
        before_b = {"easy": {"matched": 55, "unmatched": 1068, "total": 1123}}
        after_b = {"easy": {"matched": 59, "unmatched": 1064, "total": 1123}}
        d = compute_diff({}, {}, before_b, after_b)
        self.assertEqual(d.tier_summary["easy"], (55, 59))
        # TIER_ORDER entries always present even if breakdown didn't include.
        self.assertIn("trivial", d.tier_summary)
        self.assertEqual(d.tier_summary["trivial"], (0, 0))


