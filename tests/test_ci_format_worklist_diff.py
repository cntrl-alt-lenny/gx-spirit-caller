class TestParseAddr(unittest.TestCase):
    def test_hex_string(self):
        self.assertEqual(_parse_addr("0x100"), 0x100)
        self.assertEqual(_parse_addr("0X021aa4a0"), 0x021aa4a0)

    def test_int(self):
        self.assertEqual(_parse_addr(256), 256)

    def test_garbage_returns_none(self):
        self.assertIsNone(_parse_addr("not_a_number"))
        self.assertIsNone(_parse_addr(None))
        self.assertIsNone(_parse_addr([]))


class TestLoadEntries(unittest.TestCase):
    def test_roundtrip(self):
        with tempfile.TemporaryDirectory() as td:
            p = Path(td) / "snap.json"
            _write_snapshot(p, [
                {"tier": "easy", "module": "ov006",
                 "addr": "0x021ba1f0", "size": 8, "name": "x"},
            ])
            entries = _load_entries(p)
        self.assertIn(("ov006", 0x021ba1f0), entries)
        e = entries[("ov006", 0x021ba1f0)]
        self.assertEqual(e.tier, "easy")
        self.assertEqual(e.size, 8)
        self.assertEqual(e.name, "x")

    def test_missing_file_returns_empty(self):
        self.assertEqual(_load_entries(Path("/nonexistent")), {})

    def test_malformed_rows_skipped(self):
        # Missing module / unparseable addr must not crash.
        with tempfile.TemporaryDirectory() as td:
            p = Path(td) / "snap.json"
            _write_snapshot(p, [
                {"tier": "easy", "module": "ov006",
                 "addr": "0x100", "size": 8, "name": "ok"},
                {"tier": "easy", "addr": "0x200", "size": 8},       # no module
                {"tier": "easy", "module": "ov006", "addr": "bad"},  # no addr
            ])
            entries = _load_entries(p)
        self.assertEqual(len(entries), 1)
        self.assertIn(("ov006", 0x100), entries)


class TestLoadBreakdown(unittest.TestCase):
    def test_missing_file_returns_empty(self):
        self.assertEqual(_load_breakdown(Path("/nonexistent")), {})

    def test_present_breakdown(self):
        with tempfile.TemporaryDirectory() as td:
            p = Path(td) / "snap.json"
            _write_snapshot(p, [], breakdown={
                "easy": {"matched": 5, "unmatched": 100, "total": 105},
            })
            b = _load_breakdown(p)
        self.assertEqual(b["easy"]["matched"], 5)

    def test_corrupt_json_returns_empty(self):
        with tempfile.TemporaryDirectory() as td:
            p = Path(td) / "snap.json"
            p.write_text("{not valid json")
            self.assertEqual(_load_breakdown(p), {})


class TestSortHelpers(unittest.TestCase):
    def test_tier_rank_ordering(self):
        self.assertLess(_tier_rank("trivial"), _tier_rank("easy"))
        self.assertLess(_tier_rank("easy"), _tier_rank("hard"))

    def test_unknown_tier_sorts_last(self):
        self.assertEqual(_tier_rank("unknown"), len(TIER_ORDER))

    def test_entry_sort_key_tier_first(self):
        easy = _entry("ov005", 0x100, tier="easy", size=0x40)
        triv = _entry("ov006", 0x200, tier="trivial", size=0x20)
        # trivial < easy in sort order regardless of size.
        self.assertLess(_entry_sort_key(triv), _entry_sort_key(easy))


